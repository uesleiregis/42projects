#include "ft_printf.h"

int	ft_putnbr_u_fd(unsigned int n, int fd)
{
	char	c;

	if (n >= 10)
	{
		if (ft_putnbr_u_fd(n / 10, fd) == -1)
			return (-1);
	}
	c = (n % 10) + '0';
	return (write(fd, &c, 1));
}
